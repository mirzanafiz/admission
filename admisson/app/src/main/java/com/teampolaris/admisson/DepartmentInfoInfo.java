public class DepartmentInfoInfo
{
    String id;
    String unitId;
    String departmentId;
    String noOfSeats;
    String examinationDetail;
    String deptName;

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    public String getExaminationDetail() {
        return examinationDetail;
    }

    public void setExaminationDetail(String examinationDetail) {
        this.examinationDetail = examinationDetail;
    }

    public String getDepartmentName() {
        return departmentId;
    }

    public void setDepartmentName(String departmentId) {
        this.departmentId = departmentId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNoOfSeats() {
        return noOfSeats;
    }

    public String getUnitId() {
        return unitId;
    }

    public void setUnitId(String unitId) {
        this.unitId = unitId;
    }

    public String getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(String departmentId) {
        this.departmentId = departmentId;
    }

    public void setNoOfSeats(String noOfSeats) {
        this.noOfSeats = noOfSeats;
    }


}
